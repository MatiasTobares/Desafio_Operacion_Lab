using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Desafio_Lab
{
    class Calculos
    {
        //Método estático que realiza la suma
        public static int suma(int a, int b)
        {
            return a + b;
        }

        //Método estático que realiza la resta
        public static int resta(int a, int b)
        {
            return a - b;
        }

    }
}
